DROP TABLE IF EXISTS generator_domain;
CREATE TABLE generator_domain (
    domain_id      VARCHAR(255) NOT NULL PRIMARY KEY, -- '领域ID'
    context_id     VARCHAR(255) NOT NULL,             -- '限界上下文ID'
    name           VARCHAR(255) NOT NULL,             -- '领域名称'
    description    VARCHAR(255) NOT NULL,             -- '领域描述'
    version        VARCHAR(255) NOT NULL,             -- '领域版本'
    author         VARCHAR(255) NOT NULL,             -- '领域作者'
    create_at      BIGINT       NOT NULL,             -- '领域创建时间'
    modify_at      BIGINT       NOT NULL,             -- '领域修改时间'
    create_user_id VARCHAR(255) NOT NULL,             -- '创建者ID'
    modify_user_id VARCHAR(255) NOT NULL,             -- '修改者ID'
    is_removed     BOOLEAN      NOT NULL,             -- '是否已删除'
    remove_at      BIGINT       NULL DEFAULT 0        -- '删除时间'
); -- '领域表'
